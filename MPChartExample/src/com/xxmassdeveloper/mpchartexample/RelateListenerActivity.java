        ChartTouchListener listener1 = new BarLineChartTouchListener(mChart, mChart.getViewPortHandler().getMatrixTouch(), 3);
        ChartTouchListener listener2 = new BarLineChartTouchListener(mChart2, mChart2.getViewPortHandler().getMatrixTouch(), 3);
        ChartTouchListener listener3 = new BarLineChartTouchListener(mChart3, mChart3.getViewPortHandler().getMatrixTouch(), 3);
        mChart.setOnTouchListener(listener1);
